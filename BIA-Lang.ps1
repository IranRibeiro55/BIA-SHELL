function Get-BIAStr {
    param([string]$Key)
    $t = $script:BIA_Strings[$script:BIA_Lang]
    if ($t -and $t.ContainsKey($Key)) { return $t[$Key] }
    $t = $script:BIA_Strings['pt']
    if ($t -and $t.ContainsKey($Key)) { return $t[$Key] }
    if ($Key -eq 'N/A') { return 'N/A' }
    return $Key
}
